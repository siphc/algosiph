class Binary_Tree:
    def __init__(self, val) -> None:
        self.val = val
        self.left = None
        self.right = None

class Tree:
    def __init__(self, val) -> None:
        self.val = val
        self.children = []

class SLL:
    def __init__(self, val) -> None:
        self.val = val
        self.next = None

class DLL:
    def __init__(self, val) -> None:
        self.val = val
        self.prev = None
        self.next = None